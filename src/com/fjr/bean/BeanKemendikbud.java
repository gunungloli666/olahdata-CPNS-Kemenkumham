package com.fjr.bean;

public class BeanKemendikbud {
	
	private String nama; 
	private String kodeKTP;
	private String noPendaftaran;
	private String unitPenempatan;
	public String getNama() {
		return nama;
	}
	public void setNama(String nama) {
		this.nama = nama;
	}
	public String getKodeKTP() {
		return kodeKTP;
	}
	public void setKodeKTP(String kodeKTP) {
		this.kodeKTP = kodeKTP;
	}
	public String getNoPendaftaran() {
		return noPendaftaran;
	}
	public void setNoPendaftaran(String noPendaftaran) {
		this.noPendaftaran = noPendaftaran;
	}
	public String getUnitPenempatan() {
		return unitPenempatan;
	}
	public void setUnitPenempatan(String unitPenempatan) {
		this.unitPenempatan = unitPenempatan;
	} 
	
	

}
